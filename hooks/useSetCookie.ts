function setCookie(name: string, value: string, hours: number = 1) {
  let expires = "";
  if (hours) {
    const date = new Date();
    date.setTime(date.getTime() + hours * 60 * 60 * 1000);
    expires = "; expires=" + date.toUTCString();
  }
  let cookieOptions = "; path=/";
  if (process.env.NODE_ENV === "production") {
    cookieOptions = cookieOptions + "; HttpOnly; Secure";
  }
  document.cookie = name + "=" + (value || "") + expires + cookieOptions;
}

function getCookie(name: string) {
  const nameEQ = name + "=";
  const ca = document.cookie.split(";");
  for (let i = 0; i < ca.length; i++) {
    let c = ca[i];
    while (c.charAt(0) == " ") c = c.substring(1, c.length);
    if (c.indexOf(nameEQ) == 0) return c.substring(nameEQ.length, c.length);
  }
  return null;
}

function getCookieFromString(cookieString: string, name: string) {
  const nameEQ = name + "=";
  const ca = cookieString.split(";");
  for (let i = 0; i < ca.length; i++) {
    let c = ca[i];
    while (c.charAt(0) == " ") c = c.substring(1, c.length);
    if (c.indexOf(nameEQ) == 0) return c.substring(nameEQ.length, c.length);
  }
  return undefined;
}
function eraseCookie(name: string) {
  document.cookie = name + "=; Path=/; Expires=Thu, 01 Jan 1970 00:00:01 GMT;";
}

export { setCookie, getCookie, eraseCookie, getCookieFromString };
